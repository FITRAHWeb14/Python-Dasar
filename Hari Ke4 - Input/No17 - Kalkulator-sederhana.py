perkalian = angka1 * angka2
pembagian = angka1 / angka2

print(f"kamu memasukkan angka ke 1 : {angka1}")
print(f"kamu memasukkan angka ke 2 : {angka2}")
print(f"hasil dari penjumlahan : {penjumlahan}")
print(f"hasil dari penguranga : {pengurangan}")
print(f"hasil dari perkalian : {perkalian}")
print(f"hasil dari pembagian : {pembagian}")