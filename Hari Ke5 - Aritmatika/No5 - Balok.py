Panjang = int(input(f"masukkan panjang balok : "))
Lebar = int(input(f"masukkan lebar balok : "))
tinggi_balok = int(input(f"masukkan tinggi balok : "))

volume_balok = Panjang * Lebar * tinggi_balok

print(f"panjang balok : {Panjang}")
print(f"lebar balok : {Lebar}")
print(f"tinggi balok : {tinggi_balok}")
print(f"hasil : {volume_balok}")
