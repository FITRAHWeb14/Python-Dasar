AngkaA = int(input(f"masukkan angka A : "))
AngkaB = int(input(f"masukkan angka B : "))
AngkaC = int(input(f"masukkan angka C : "))

Hitung_Awal = (AngkaA + AngkaB) * AngkaC
Hitung_Dua = AngkaA + (AngkaB * AngkaC)

print(f"Angka A dimasukkan : {AngkaA}")
print(f"Angka B dimasukkan : {AngkaB}")
print(f"Angka C dimasukkan : {AngkaC}")
print(f"hasil awal adalah : {Hitung_Awal}")
print(f"hasil kedua : {Hitung_Dua}")